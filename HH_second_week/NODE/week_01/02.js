// 데이터 타입
// runtime : run 하는 time
// 코드가 실행될 때 데이터 타입이 결정된다

// 1. int
// 1-1 정수
let num1 = 10
console.log(num1)
console.log(typeof num1)

// 1-2 실수형 (float) 소숫점

let num2 = 3.14
console.log(num2)
console.log(typeof num2)

// 1-3 지수형 (Exp)
let num3 = 2.5e5 // 2.5 x 10^5
console.log(num3)

// 1-4
// Nan : Not a Number
let num4 = "hello" /2
console.log(num4)

// 1-5 infinity 
let num5 = 1 / 0
console.log (num5)

// 1-6 infinity 
let num6 = -1 / 0
console.log (num6)
